package com.kiosk.app.infra.stamp;



public class Stamp {

	private String stampSeq;

	private String stampNy;

	private String countStampUseDate;

	private String customer_seq;

	private String phoneNum;







//	----------------------------------------


	public String getStampSeq() {
		return stampSeq;
	}

	public void setStampSeq(String stampSeq) {
		this.stampSeq = stampSeq;
	}

	public String getStampNy() {
		return stampNy;
	}

	public void setStampNy(String stampNy) {
		this.stampNy = stampNy;
	}

	public String getCountStampUseDate() {
		return countStampUseDate;
	}

	public void setCountStampUseDate(String countStampUseDate) {
		this.countStampUseDate = countStampUseDate;
	}

	public String getCustomer_seq() {
		return customer_seq;
	}

	public void setCustomer_seq(String customer_seq) {
		this.customer_seq = customer_seq;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}
}