package com.kiosk.app.infra.optionList;

public class OptionList {
	private String seq;
	private String optionQuantity;
	private String option_seq;
	private String orderList_orderListId;
	
	
	
//	----------------------------------------
	
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getOptionQuantity() {
		return optionQuantity;
	}
	public void setOptionQuantity(String optionQuantity) {
		this.optionQuantity = optionQuantity;
	}
	public String getOption_seq() {
		return option_seq;
	}
	public void setOption_seq(String option_seq) {
		this.option_seq = option_seq;
	}
	public String getOrderList_orderListId() {
		return orderList_orderListId;
	}
	public void setOrderList_orderListId(String orderList_orderListId) {
		this.orderList_orderListId = orderList_orderListId;
	}
	
}